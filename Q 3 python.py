#For a=56 and b=10 with the help of bitwise operators calculate the following:#
a = 56
b = 10
#(i) a&b#
c = a&b
print(c)
#(ii) a|b#
d = a|b
print(d)
#(iii) a^b#
e = a^b
print(e)
#(iv) Left shift both a and b with 2 bits.#
f = a<<2, b<<2
print(f)
#(v) Right shift a with 2 bits and b with 4 bits.#
g = a>>2, b>>4
print(g)
